package android.batch1;

public class SubCategoryList {

    String image,name;
    int outlets,totalDeal,freeDeal;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getOutlets() {
        return outlets;
    }

    public void setOutlets(int outlets) {
        this.outlets = outlets;
    }

    public int getTotalDeal() {
        return totalDeal;
    }

    public void setTotalDeal(int totalDeal) {
        this.totalDeal = totalDeal;
    }

    public int getFreeDeal() {
        return freeDeal;
    }

    public void setFreeDeal(int freeDeal) {
        this.freeDeal = freeDeal;
    }
}
